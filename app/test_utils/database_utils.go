// SetupDB is a utility function to setup a testing db
func SetupDB(a *common.App) {
  if err := a.DB.Exec(setupDBQuery).Error; err != nil {
    log.Fatal(err)
  }
  if err := a.DB.Exec("select * from review").Error; err != nil {
    log.Error(err)
  }
}

